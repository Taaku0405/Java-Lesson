class Main {
  public static void main(String[] args) {
    // Javaの基本を覚えるための練習
    System.out.println("Hello Java");

    // 数値と文字列の違いを学ぶ
    // 数値の17を出力
    System.out.println(17);

    // 5に3を足した値を出力
    System.out.println(5 + 3);

    // [5 + 3]を文字列で出力
    System.out.println("5 + 3");

    // 12を3で割った値を出力
    System.out.println(12 / 3);

    // 3に6を掛けた値を出力
    System.out.println(3 * 6);

    // 8を3で割った時の余りの値を出力
    System.out.println(8 % 3);
  }
}